/* Charge Pump */
#define PIN_CP PA0
#define DDR_CP DDRA
#define PORT_CP PORTA

/* Flash Inhibit Signal */
#define PIN_INHIBIT PA2
#define DDR_INHIBIT DDRA
#define PORT_INHIBIT PORTA

/* Kill Signal */
#define PIN_KILL  PA3
#define DDR_KILL  DDRA
#define PORT_KILL PORTA

/* LED */
#define PIN_LED   PA7
#define DDR_LED   DDRA
#define PORT_LED  PORTA
